class Solution(object):
    def plusOne(self, digits):
        """
        :type digits: List[int]
        :rtype: List[int]
        """
        s = ''
        for i in digits:
            s = s + str(i)
        k = int(s) + 1
        l = []
        for i in str(k):
            l.append(i)
        return l
